import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { resolveAndroidSdk } from "./resolve-android-sdk.mjs";

const mobileRoot = path.join(path.dirname(fileURLToPath(import.meta.url)), "..");
const androidDir = path.join(mobileRoot, "android");
const sdk = resolveAndroidSdk().replace(/\\/g, "/");

if (!fs.existsSync(androidDir)) {
  console.warn("android/ not found — run prebuild first.");
  process.exit(0);
}

const content = `## Generated by scripts/write-local-properties.mjs — do not commit\nsdk.dir=${sdk}\n`;
fs.writeFileSync(path.join(androidDir, "local.properties"), content);
console.log(`Wrote android/local.properties → sdk.dir=${sdk}`);
